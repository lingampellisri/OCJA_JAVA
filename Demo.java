package data;

// Class to which the above package belongs
public class Demo {

protected String name="Dom";

    // Member functions of the class- 'Demo'
    // Method 1 - To show()
    protected void show()
    {

        // Print message
        System.out.println("Hi Everyone");
    }

    // Method 2 - To show()
    public void view()
    {
        // Print message
        System.out.println("Hello");
    }
}
